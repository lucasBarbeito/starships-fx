package edu.austral.dissis.starships.dataStructure;

public class DimensionTuple {
    double width;
    double height;

    public DimensionTuple(double width, double height) {
        this.width = width;
        this.height = height;
    }

    public double getHeight() {
        return height;
    }

    public double getWidth() {
        return width;
    }

    public void setHeight(double height) {
        this.height = height;
    }

    public void setWidth(double width) {
        this.width = width;
    }
}
